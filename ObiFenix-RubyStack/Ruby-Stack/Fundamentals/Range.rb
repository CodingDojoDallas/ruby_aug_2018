# Challenge: Useful Methods for arrays
# ====================================

# Challenge #1: .include?(value) => true or false
# -----------------------------------------------
puts (1..50).include?(15)


# Challenge #2: .last => returns the last object in rang
# -------------------------------------------------------
puts (1..50).last()


# Challenge #3: .max  => returns the maximum value in range
# ---------------------------------------------------------
puts (1..50).max()


# Challenge #4  .min  => returns the minimum value in range
# ---------------------------------------------------------
puts (1..50).min()


